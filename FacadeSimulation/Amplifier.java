public class Amplifier {
    public void on() {
        System.out.println("Amplificador encendido.");
    }

    public void setVolume(int level) {
        System.out.println("Volumen del amplificador ajustado a " + level);
    }
    
    public void off() {
        System.out.println("Amplificador apagado.");
    }
}
